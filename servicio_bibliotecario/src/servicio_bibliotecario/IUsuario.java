	private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {
		// Agregar un nuevo usuario - Tipo 0: normal, Tipo 1: estudiante
        Usuario u = new Usuario();
        u.setIdUsuario(Integer.parseInt(jTextField1.getText()));
        u.setNombre(jTextField2.getText());
        u.setApellidoP(jTextField3.getText());
        u.setApellidoM(jTextField4.getText());
        u.setDireccion(jTextField5.getText());
        u.setTipoUsuario(jComboBox1.getSelectedIndex());
        usuarios.add(u);
        JOptionPane.showMessageDialog(null,"Usuario agregado");
        System.out.println(usuarios);    
        jTextField1.setText("");
        jTextField2.setText("");
        jTextField3.setText("");
        jTextField4.setText("");
        jTextField5.setText("");
	}
	
